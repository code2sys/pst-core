  public function createQueue($id = '0000', $type = 'queryItem', $test = 0)
  {
    switch($type)
    {
      case 'queryItem':
        $this->quickbooks->enqueue(QUICKBOOKS_QUERY_ITEM, $id);
        break;
      case 'queryCustomer':
        $this->quickbooks->enqueue(QUICKBOOKS_QUERY_CUSTOMER, $id);
        break; 
      case 'querySalesOrder':
        $this->quickbooks->enqueue(QUICKBOOKS_QUERY_SALESORDER, $id);
        break;
      case 'queryPriceLevel':
        $this->quickbooks->enqueue(QUICKBOOKS_QUERY_PRICELEVEL, $id);
        break;
      case 'addSalesOrder':
        $this->quickbooks->enqueue(QUICKBOOKS_ADD_SALESORDER, $id);
        break;
      
        
    }
    if($test)
      echo "Queue Created ".$id;
    return TRUE;
  }
  
